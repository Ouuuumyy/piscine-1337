#include <stdio.h>

void	ft_ultimate_div_mod(int *a, int *b)
{
	int	pt1;
	int	pt2;

	pt1 = *a;
	pt2 = *b;
	*a = pt1 / pt2;
	*b = pt1 % pt2;
}
