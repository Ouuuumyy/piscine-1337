#include <unistd.h>

void	ft_putnbr(int nb)
{
	int	i;
	int	n;
	char	c[10];

	i = 0;
	n = nb;
	if (n == 0)
	{
		write(1, "0", 1);
		return ;
	}
	else if (n < 0)
	{
		write(1, "-", 1);
		n *= -1;
	}
	while (n > 0)
	{
		c[++i] = (n % 10) + 48;
		n = n / 10;
	}
	while (i >= 0)
	{
		write(1, &c[i--], 1);
	}
}
