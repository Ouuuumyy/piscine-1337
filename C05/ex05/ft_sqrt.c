#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	i;

	i = 2;
	if (nb == 0)
		return (0);
	if (nb == 1)
		return (1);
	if (nb >= 2)
	{
		while (i <= nb / 2)
		{
			if (nb == i * i)
				return (i);
			i++;
		}
	}
	return (0);
}
